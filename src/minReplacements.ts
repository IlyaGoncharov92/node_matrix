
/**
 * Функция подсчитывает минимальное количество замен, чтобы в массиве
 * не встречалось 3 положительных или 3 отрицательных числа подряд.
 * @returns Минимальное количество замен
 */
export function minReplacements(numbers: number[]): number {
	if (numbers.length !== 10) {
		throw new Error("Массив должен содержать ровно 10 элементов");
	}

	// Копируем массив, чтобы не мутировать исходный,
	// так как нам нужно "помечать" измененные элементы
	const arr = [...numbers];
	let changes = 0;

	// Идем по массиву, начиная с третьего элемента (индекс 2)
	for (let i = 2; i < arr.length; i++) {
		const a = arr[i - 2];
		const b = arr[i - 1];
		const c = arr[i];

		// Проверяем, имеют ли три числа подряд одинаковый знак.
		// Ноль считаем нейтральным числом (ни положительным, ни отрицательным).
		const allPositive = a > 0 && b > 0 && c > 0;
		const allNegative = a < 0 && b < 0 && c < 0;

		if (allPositive || allNegative) {
			changes++;
			// "Заменяем" текущее число на 0.
			// 0 не является ни положительным, ни отрицательным,
			// поэтому он гарантированно разбивает последовательность.
			arr[i] = 0;
		}
	}

	return changes;
}
